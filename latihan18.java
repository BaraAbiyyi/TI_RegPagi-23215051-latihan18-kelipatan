package pertemuan5;

public class latihan18 {
    public static void main(String[] args) {
        // Mulai dari 3.5, kelipatan 3.5
        double angka = 3.5;
        
        // Looping dari 3.5 sampai dengan 35.0
        while (angka <= 35.0) {
            System.out.println(angka);
            angka += 3.5; // Tambahkan 3.5 setiap iterasi
        }
    }
}


